module Task11

let main n =
    if n < 2
    then 
        n
    else 
        let C = array2D [| [|0; 0|]; [|0; 0|] |]
        let B = array2D [| [|1; 1|]; [|1; 1|] |]
        let A = array2D [| [|1; 1|]; [|1; 0|] |]
        for i in 2..n do
            C.[0, 0] <- B.[0, 0] * A.[0, 0] + B.[0, 1] * A.[1, 0]
            C.[0, 1] <- B.[0, 0] * A.[0, 1] + B.[0, 1] * A.[1, 1]
            C.[1, 0] <- B.[1, 0] * A.[0, 0] + B.[1, 1] * A.[1, 0]
            C.[1, 1] <- B.[1, 0] * A.[0, 1] + B.[1, 1] * A.[1, 1]
            B.[0, 0] <- C.[0, 0]
            B.[0, 1] <- C.[0, 1]
            B.[1, 0] <- C.[1, 0]
            B.[1, 1] <- C.[1, 1]
        B.[0, 1]